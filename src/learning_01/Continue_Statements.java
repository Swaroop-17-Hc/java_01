package learning_01;

public class Continue_Statements {
	public static void main(String args[]) {
		
		for(int i=0;i<=10;i++) {
			if(i==3 || i==7) {
				continue;
			}
			
			//System.out.println(i);
		}
		
		//find the largest of 4 numbers
		for(int j=0;j<=10;j++) {
		if(j==0 && j==10) {
			System.out.println(j);
			}
	}

}
}
 